function buildTransactionsAndReceipts(): { transactions: Transaction[]; receipts: Receipt[] } {
  const transactions: Transaction[] = [];
  const receipts: Receipt[] = [];
  for (const spec of TX_SPECS) {
    const { barberEarning, shopEarning } = splitEarnings(spec.total, spec.commissionPercent);
    transactions.push({
      id: spec.id,
      branchId: spec.branchId,
      barberProfileId: spec.barberProfileId,
      serviceId: spec.serviceId,
      customerId: spec.customerId,
      customerNameSnapshot: spec.customerName,
      customerPhoneSnapshot: spec.customerPhone ?? null,
      customerWhatsappSnapshot: spec.customerWhatsapp ?? null,
      serviceNameSnapshot: spec.serviceName,
      totalAmount: spec.total,
      commissionPercentSnapshot: spec.commissionPercent,
      barberEarning,
      shopEarning,
      paymentMethod: spec.paymentMethod,
      paymentDate: spec.paymentDate,
      notes: spec.notes ?? null,
      recordedByUserId: spec.recordedByUserId,
    });
    receipts.push({
      id: `rc-${spec.id}`,
      transactionId: spec.id,
      branchId: spec.branchId,
      receiptNumber: spec.receiptNumber,
      issuedAt: spec.paymentDate,
    });
  }
  return { transactions, receipts };
}

